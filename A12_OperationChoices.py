a=int(input("Enter 1st no.:-"))
b=int(input("Enter 2nd no.:-"))
c=input()
if c=="1":
    print(a+b)
elif c=="2":
    print(a-b)
elif c=="3":
    print(a*b)
else:
    print(a//b)

# Input
# c :1
# a:12
# b:16
# Output:
# Since ‘c’=1 , (12+16) is performed which is equal to 28 , hence 28 is returned.
